package day019_Loops;

public class DoWhileRunOnce {

	public static void main(String[] args) {
		
		// in this example, do while loop body will run
		//once the test the condition, then exit the loop,
		//as condition false

		int count = 10;
		
		do {
			System.out.println("Count: "+count);
			count++;
		}while (count <=5);
		
		
		
		
	}

}
